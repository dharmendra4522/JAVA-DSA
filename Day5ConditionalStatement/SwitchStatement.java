package Day5ConditionalStatement;

public class SwitchStatement {
    public static void main(String[] args) {

        int number = 2;
        switch (number) {

            case 1:
                System.out.println("Samosa");
                break;
            case 2:
                System.out.println("barger");
                break;
            case 3:
                System.out.println("we wake up");
                break;
            default:
                System.out.println("we wake up");
        }
    }
}