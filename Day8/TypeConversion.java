package Day8;

public class TypeConversion {
    public static void main(String[] args) {
     short s = 2;
     char r = 'a';   //97
     int x = 5;
     int y = s+r+x;
     System.out.println(y);
     double d=2.5;
     double  z = s+r+x+d; 
     System.out.println(z);
    }
    
}
