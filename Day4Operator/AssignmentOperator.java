package Day4Operator;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        // a = a + 10;
        a += 10;
        System.out.println(a);
        int b = 5;
        // b = b - 5;
        b -=5;
        System.out.println(b);

        int c = 5;
        // c = c * 5;
        c *=5;
        System.out.println(c);

        int d = 5;
        // d = d / 5;
        d /=5;
        System.out.println(d);

        int e = 5;
        // e = e % 5;
        e %=5;
        System.out.println(e);
    }
    
}
