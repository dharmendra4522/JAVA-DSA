package Day4Operator;

public class RelationalOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println(a==b); //(is equal?)for equality checking return(true/false)
        System.out.println(a!=b); //(is not equal?)for equality checking return(true/false)
        System.out.println(a>b); //(greater then ?)for grater value  checking return(true/false)
        System.out.println(a<b); //(greater then ?)for less value checking return(true/false)
        System.out.println(a<=b); //(greater then eqaul to ?)for greater then or equal checking return(true/false)
        System.out.println(a<=b); //(less then eqaul to ?)for less then or equal checking return(true/false)
    }
}
